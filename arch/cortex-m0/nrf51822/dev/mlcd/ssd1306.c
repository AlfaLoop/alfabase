extern const struct spi_driver spi0_driver;
// SSD1306_EXTERNALVCC
// SSD1306_SWITCHCAPVCC
static uint8_t vccstate = SSD1306_SWITCHCAPVCC;
#define ssd1306_swap(a, b) { int16_t t = a; a = b; b = t; }
/*---------------------------------------------------------------------------*/
void
ssd1306_init(void)
{
  nrf_gpio_cfg_output(RST_OLED);
  nrf_gpio_pin_set(RST_OLED);
  nrf_gpio_cfg_output(DC_OLED);
  nrf_gpio_cfg_output(SS_OLED);
  nrf_gpio_pin_set(DC_OLED);
  nrf_gpio_pin_set(SS_OLED);

	spi_master_config_t config;
	config.freq =  SPI_FREQUENCY_M4;
	config.sck_pin = CLK_OLED;
	config.mosi_pin = MOSI_OLED;
	config.miso_pin = FAKE_MISO_OLED;
	config.irq_priority = APP_IRQ_PRIORITY_LOW;
	config.order = SPI_MSB;
	config.mode = SPI_MODE3;
	spi0_driver.init(&config);

  // Init sequence
  ssd1306_command(SSD1306_DISPLAYOFF);                    // 0xAE
  ssd1306_command(SSD1306_SETDISPLAYCLOCKDIV);            // 0xD5
  ssd1306_command(0x80);                                  // the suggested ratio 0x80

  ssd1306_command(SSD1306_SETMULTIPLEX);                  // 0xA8
  ssd1306_command(SSD1306_LCDHEIGHT - 1);

  ssd1306_command(SSD1306_SETDISPLAYOFFSET);              // 0xD3
  ssd1306_command(0x0);                                   // no offset
  ssd1306_command(SSD1306_SETSTARTLINE | 0x0);            // line #0
  ssd1306_command(SSD1306_CHARGEPUMP);                    // 0x8D
  if (vccstate == SSD1306_EXTERNALVCC){
    ssd1306_command(0x10);
  }
  else {
    ssd1306_command(0x14);
  }
  ssd1306_command(SSD1306_MEMORYMODE);                    // 0x20
  ssd1306_command(0x00);                                  // 0x0 act like ks0108
  ssd1306_command(SSD1306_SEGREMAP | 0x1);
  ssd1306_command(SSD1306_COMSCANDEC);

#if defined SSD1306_128_32
  ssd1306_command(SSD1306_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x02);
  ssd1306_command(SSD1306_SETCONTRAST);                   // 0x81
  ssd1306_command(0x8F);

#elif defined SSD1306_128_64
  ssd1306_command(SSD1306_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x12);
  ssd1306_command(SSD1306_SETCONTRAST);                   // 0x81
  if (vccstate == SSD1306_EXTERNALVCC)
    { ssd1306_command(0x9F); }
  else
    { ssd1306_command(0xCF); }

#elif defined SSD1306_96_16
  ssd1306_command(SSD1306_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x2);   //ada x12
  ssd1306_command(SSD1306_SETCONTRAST);                   // 0x81
  if (vccstate == SSD1306_EXTERNALVCC) {
    ssd1306_command(0x10);
  }
  else {
    ssd1306_command(0xAF);
  }
#endif

  ssd1306_command(SSD1306_SETPRECHARGE);                  // 0xd9
  if (vccstate == SSD1306_EXTERNALVCC) {
    ssd1306_command(0x22);
  }
  else {
    ssd1306_command(0xF1);
  }
  ssd1306_command(SSD1306_SETVCOMDETECT);                 // 0xDB
  ssd1306_command(0x40);
  ssd1306_command(SSD1306_DISPLAYALLON_RESUME);           // 0xA4
  ssd1306_command(SSD1306_NORMALDISPLAY);                 // 0xA6

  ssd1306_command(SSD1306_DEACTIVATE_SCROLL);

  ssd1306_command(SSD1306_DISPLAYON);//--turn on oled panel
}
/*---------------------------------------------------------------------------*/
void
ssd1306_command(uint8_t c)
{
  nrf_gpio_pin_set(SS_OLED);
  nrf_gpio_pin_clear(DC_OLED);
  nrf_gpio_pin_clear(SS_OLED);

  spi0_driver.enable();
  spi0_driver.transfer(&c, 1, NULL, 0);
	spi0_driver.disable();

  nrf_gpio_pin_set(SS_OLED);
}
/*---------------------------------------------------------------------------*/
void
ssd1306_cleardisplay(void)
{
  memset(m_lcd_buffer, 0, (SSD1306_LCDWIDTH*SSD1306_LCDHEIGHT/8));
}
/*---------------------------------------------------------------------------*/
void
ssd1306_display(void)
{
  ssd1306_command(SSD1306_COLUMNADDR);
  ssd1306_command(0);   // Column start address (0 = reset)
  ssd1306_command(SSD1306_LCDWIDTH-1); // Column end address (127 = reset)

  ssd1306_command(SSD1306_PAGEADDR);
  ssd1306_command(0); // Page start address (0 = reset)
#if SSD1306_LCDHEIGHT == 64
  ssd1306_command(7); // Page end address
#endif
#if SSD1306_LCDHEIGHT == 32
  ssd1306_command(3); // Page end address
#endif
#if SSD1306_LCDHEIGHT == 16
  ssd1306_command(1); // Page end address
#endif
  nrf_gpio_pin_set(SS_OLED);
  nrf_gpio_pin_set(DC_OLED);
  nrf_gpio_pin_clear(SS_OLED);
  spi0_driver.enable();
  spi0_driver.transfer(m_lcd_buffer, SSD1306_LCDWIDTH*SSD1306_LCDHEIGHT/8, NULL, 0);
  spi0_driver.disable();
  nrf_gpio_pin_set(SS_OLED);
}
